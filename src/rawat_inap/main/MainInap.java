    public void UpdateKosong(String isi){
        rawat_inap.exec.ExecuteInap eBook = new rawat_inap.exec.ExecuteInap();
        int hasil2 = eBook.updateRuanganToKosong(isi);
            if(hasil2 >0){
                JOptionPane.showMessageDialog(null, "Ruangan "+isi+" Kosong.");
                setDataRuangan();
            }
            else{
                JOptionPane.showMessageDialog(null, "Data gagal di hapus");
            }
    }
    
