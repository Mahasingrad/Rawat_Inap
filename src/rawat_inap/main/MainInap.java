    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        String strkode = txtLaporan.getText();
        String strruangan = txtRuangan.getText();
        rawat_inap.exec.ExecuteInap eBook = new rawat_inap.exec.ExecuteInap();
        int hasil = eBook.deleteLaporan(strkode);
        if(hasil >0){
            JOptionPane.showMessageDialog(null, "Laporan berhasil di hapus");
            setDataLaporan();
            int hasil2 = eBook.updateRuanganToKosong(strruangan);
            if(hasil2 >0){
                JOptionPane.showMessageDialog(null, "Ruangan "+strruangan+" Kosong.");
                setDataRuangan();
            }
            else{
                JOptionPane.showMessageDialog(null, "Data gagal di hapus");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Data gagal di hapus");
        }
    }//GEN-LAST:event_btnHapusActionPerformed

